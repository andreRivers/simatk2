<?php
defined('BASEPATH') or exit('No direct script access allowed');

class Auth extends CI_Controller
{
    public function __construct()
    {
        parent::__construct();
        $this->load->library('form_validation');
    }

    public function index()
    {
        if ($this->session->userdata('username')) {
            redirect('user');
        }

        $this->form_validation->set_rules('username', 'username', 'trim|required');
        $this->form_validation->set_rules('password', 'Password', 'trim|required');

        if ($this->form_validation->run() == false) {
            $data['title'] = 'Login Page';
            $this->load->view('templates/auth_header', $data);
            $this->load->view('auth/login');
            $this->load->view('templates/auth_footer');
        } else {
            // validasinya success
            $this->_login();
        }
    }


    private function _login()
    {
        $username = $this->input->post('username');
        $password = $this->input->post('password');

        $user = $this->db->get_where('at_user', ['username' => $username])->row_array();

        // jika usernya ada
        if ($user) {
            // jika usernya aktif
            if ($user['is_active'] == 1) {
                // cek password
                if (password_verify($password, $user['password'])) {
                    $data = [
                        'username' => $user['username'],
                        'role_id' => $user['role_id']
                    ];
                    $this->session->set_userdata($data);
                    if ($user['role_id'] == 1) {
                        $log = [
                            'username' => $this->input->post('username'),
                            'log' => "Berhasil login.",
                            'created_at' => time()
                        ];
                        $this->db->insert('at_log', $log);
                        redirect('user');
                    } else {
                        $log = [
                            'username' => $this->input->post('username'),
                            'log' => "Berhasil login.",
                            'created_at' => time()
                        ];
                        $this->db->insert('at_log', $log);
                        redirect('user');
                    }
                } else {
                    $log = [
                        'username' => $this->input->post('username'),
                        'log' => "Password Salah.",
                        'created_at' => time()
                    ];
                    $this->db->insert('at_log', $log);
                    $this->session->set_flashdata('message', '<div class="alert alert-danger" role="alert">Wrong password!</div>');
                    redirect('auth');
                }
            } else {
                $log = [
                    'username' => $this->input->post('username'),
                    'log' => "Username Tidak Aktif.",
                    'created_at' => time()
                ];
                $this->db->insert('at_log', $log);
                $this->session->set_flashdata('message', '<div class="alert alert-danger" role="alert">This username has not been activated!</div>');
                redirect('auth');
            }
        } else {
            $log = [
                'username' => $this->input->post('username'),
                'log' => "Username Tidak Terdaftar.",
                'created_at' => time()
            ];
            $this->db->insert('at_log', $log);
            $this->session->set_flashdata('message', '<div class="alert alert-danger" role="alert">username is not registered!</div>');
            redirect('auth');
        }
    }

